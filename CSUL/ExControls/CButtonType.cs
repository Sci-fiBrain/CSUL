namespace CSUL.ExControls
{
    /// <summary>
    /// <see cref="CButton"/>的类型枚举
    /// </summary>
    public enum CButtonType
    {
        /// <summary>
        /// 图像按钮
        /// </summary>
        Icon,

        /// <summary>
        /// 不旋转的图像按钮
        /// </summary>
        IconUnRotate,

        /// <summary>
        /// 纯文本按钮
        /// </summary>
        Text,

        /// <summary>
        /// 图标带文本按钮
        /// </summary>
        IconText,

        /// <summary>
        /// 路径按钮
        /// </summary>
        Path
    }
}